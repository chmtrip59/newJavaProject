public class forLoop {
    public static void main(String[] args) {
        int i = 1;

        //While Loop
        while(i <= 100){
            System.out.print(i + " ");
            i++;
        }
        System.out.println();

        //For Loop
        for(int j = 1 ; j<= 100; j++){
            System.out.print(j+ " ");
        }
    }
}
